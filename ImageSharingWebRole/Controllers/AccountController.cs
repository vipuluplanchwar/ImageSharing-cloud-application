        [HttpGet]
        [Authorize(Roles = "Admin")]
        public ActionResult Deactivate()
        {
            CheckAda();
            ViewBag.Message = string.Empty;

            return View(GetUsers());
        }

        [HttpPost]
        [Authorize(Roles = "Admin")]
        [ValidateAntiForgeryToken]
        public ActionResult Deactivate(IList<SelectItemView> model)
        {
            CheckAda();
            ViewBag.Message = string.Empty;

            using (ApplicationDbContext db = new ApplicationDbContext())
            {
                foreach (var item in model)
                {
                    ApplicationUser User = db.Users.Find(item.Id);

                    if (User != null)
                    {
                        if (User.Active && item.Active)
                        {
                            List<Image> UserImages = User.Images.ToList<Image>();
                            List<int> ImagesIds = new List<int>();

                            foreach (var image in UserImages)
                            {
                                db.Images.Remove(image);
                                if (image.Id != 1)
                                {
                                    ImagesIds.Add(image.Id);
                                }
                            }
                            
                            db.Users.Find(item.Id).Active = false;
                            ImageStorage.DeleteBlobs(ImagesIds, Server);
                        }
                        else if (!User.Active && !item.Active)
                        {
                            db.Users.Find(item.Id).Active =true;
                        }
                    }
                }
                db.SaveChanges();
                ViewBag.Message = "User(s) updated.";
            }
            return View(GetUsers());
        }

